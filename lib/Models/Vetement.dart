class Vetement
{
  //Variables de la classe
  String genre;
  String taille;
  String couleur;
  String description;
  String type;
  double prix;

  //Constructeur de la classe
  Vetement(String genre, String taille, String couleur, String description, String type, double prix)
  {
    this.genre = genre;
    this.taille = taille;
    this.couleur = couleur;
    this.description = description;
    this.type = type;
    this.prix = prix;
  }




}